    private void btn_pagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pagoActionPerformed
        //Obtendo o id do cliente
        int idCliente = controle.getIdAtualCliente();
        int idFormPag;
        ArrayList res;
        
        //Se o idCliente não estiver no valor padrão (-1)
        if(idCliente > -1){
            try {
                //Selecionando o id da forma de pagamento escolhida
                String query = "SELECT FORM_ID FROM TBL_FORMA_PAG WHERE FORM_DESCRICAO = '"
                        +(String) jcbx_form_pag.getSelectedItem()+"';";
                res = Conector.selecionar(query, "FORM_ID");
                idFormPag = Integer.parseInt((String) res.get(0));
                
                //Atualizando cliente como ativo e pagamento realizado
                query = "UPDATE TBL_CLIENTE SET CLI_ATIVO = TRUE WHERE CLI_ID = "+idCliente+";"
                                + "UPDATE TBL_PAGAMENTOS SET PAG_PAGO = TRUE WHERE PAG_CLI_ID = "
                                + idCliente +" and PAG_PAGO = 'f';";
                Conector.atualizar(query);
                
                //Atualizando data do próximo pagamento
                Conector.atualizarDataPagamento(idCliente, idFormPag);
                
                //Mostrando mensagem de confirmação
                JOptionPane.showMessageDialog(this, "Pagamento registrado!");
                
                //Atualizando campos da janela de controle
                controle.inicializarCampos();
                controle.setEnabled(true);
                controle.setVisible(true);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(GUI_Principal.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Ocorreu um erro de conexão com o banco de dados.");
            }
        }
    }//GEN-LAST:event_btn_pagoActionPerformed

