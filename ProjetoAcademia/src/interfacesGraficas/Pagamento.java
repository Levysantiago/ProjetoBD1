    /**
     * Inicializa o estado dos campos (Text field, Combo Box...) campos deste JFrame.
     * 
     * @return void
    */
    private void inicializarCampos(){
        ArrayList<String> results;
        String query;
        String[] data;
        
        tf_nome.setEditable(false);
        tf_sobrenome.setEditable(false);
        tf_codigo.setEditable(false);
        tf_nome.setText(controle.getTf_nomeTxt());
        tf_sobrenome.setText(controle.getTf_sobrenomeTxt());
        tf_codigo.setText(controle.getTf_codigoTxt());
        tf_valor.setEditable(false);
        
        try {
            tf_valor.setText("R$ "+Conector.getValorContrato(Conector.getIdContrato(controle.getIdAtualCliente())));
        } catch (Exception ex) {
            Logger.getLogger(Pagamento.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jcbx_form_pag.removeAllItems();
        try {    
            //Obtendo todas as formas de pagamento
            query = "SELECT FORM_DESCRICAO FROM TBL_FORMA_PAG;";
            results = Conector.selecionar(query, "FORM_DESCRICAO");
            data = results.toArray(new String[results.size()]);
            
            for(String el : data){
                jcbx_form_pag.addItem(el);
            }
        } catch (Exception ex) {
            Logger.getLogger(Pagamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

